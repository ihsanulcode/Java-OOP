
public class SwitchCase {

	public static void main(String[] args) {
		// Java Switch 
		
		int n = 5;
		
		//Syntax
		
		switch(n%2) {
			case 0:
				//work done
				System.out.println("Even");
				break;
			case 1:
				//work done
				System.out.println("Not Even");
				break;
			default:
				//work done
				System.out.println("Odd");
		}
		
		System.out.println("Rest of the code..");
		

	}

}
